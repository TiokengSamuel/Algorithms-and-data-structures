stack.append('b')
stack.append('c')

print("Initial Stack")
print(stack)

#pop function pops out value in LIFO

print("\n Elements with pop function")
print(stack.pop())
print(stack.pop())
print(stack.pop())

#Stack after pop
print("\n After pop stack")
print(stack)

